
public class ValueName {
	
	public static void main(String[] args) {
		int myInt = 10;
		
		String text = "Hello";
		
		String blank = " ";
		
		String name = "Matt";
		
		String greeting = text + blank + name;
		
		System.out.println(greeting);
		System.out.println("My integer is " + myInt);
		
		double myDouble = 7.8;
		
		System.out.println("My number is: " + myDouble + ".");
	}
}
